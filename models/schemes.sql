create table
  public.users (
    id bigint generated by default as identity,
    "Fname" text null,
    "Lname" text null,
    "Email" text not null,
    "Password" text null,
    "Phone" text null,
    "Gender" character varying null,
    "Address" text null,
    "Age" date null,
    role text null,
    constraint users_pkey primary key (id, "Email"),
    constraint users_Email_key unique ("Email")
  ) tablespace pg_default;

  create table
  public.applicants (
    "Fname" text null,
    "Lname" text null,
    "Email" text not null,
    "Phone" integer null,
    "Gender" character varying null,
    "Address" text null,
    "Age" date null,
    constraint applicants_Email_key unique ("Email")
  ) tablespace pg_default;

