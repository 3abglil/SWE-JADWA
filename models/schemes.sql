create table
  public."CarPackages" (
    id bigint generated by default as identity,
    price double precision not null,
    type character varying null,
    "Personal_Accident" character varying null,
    "COinsurance" character varying null,
    "AgencyCOinsurance" character varying null,
    "Roadside_Assistance" character varying null,
    "Civil_Liability" character varying null,
    "Police_Report" character varying null,
    "Dfixing" character varying null,
    providerid bigint null,
    constraint CarPackages_pkey primary key (id),
    constraint CarPackages_providerid_fkey foreign key (providerid) references "Providers" (id)
  ) tablespace pg_default;

create table
  public."Providers" (
    id bigint generated by default as identity,
    name text not null,
    image text null,
    constraint Providers_pkey primary key (id)
  ) tablespace pg_default;

